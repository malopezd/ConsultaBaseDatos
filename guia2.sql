SELECT
     NOMBRE_EMP||' '||
     APPATERNO_EMP||' '||
     APMATERNO_EMP AS "NOMBRE EMPLEADO",
     SUELDO_EMP AS "SUELDO ACTUAL",
     SUELDO_EMP * 108.5/100 AS "SUELDO REAJUSTADO",
     SUELDO_EMP * 8.5/100 AS "AUMENTO"
FROM EMPLEADO
ORDER BY SUELDO_EMP * 8.5/100 DESC;
--SIMULACIÓN 2
SELECT
     NOMBRE_EMP||' '||APPATERNO_EMP||' '||APMATERNO_EMP AS "NOMBRE EMPLEADO",
     SUELDO_EMP AS "SUELDO ACTUAL",
     SUELDO_EMP * 120/100 AS "SUELDO REAJUSTADO",
     SUELDO_EMP * 20/100 AS "AUMENTO"
FROM EMPLEADO
WHERE SUELDO_EMP BETWEEN 200000 AND 400000
ORDER BY SUELDO_EMP * 20/100 DESC;
