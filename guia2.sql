SELECT
    NUMRUT_CLI ||'-'||
    DVRUT_CLI AS "RUT CLIENTE",
    NOMBRE_CLI ||' '|| 
    APPATERNO_CLI ||' '|| 
    APMATERNO_CLI AS "NOMBRE CLIENTE",
    RENTA_CLI AS "RENTA",
    FONOFIJO_CLI AS "TELEFONO FIJO",
    CELULAR_CLI AS "CELULAR"
