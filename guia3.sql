SELECT 
    NUMRUN_CLI AS "RUN CLIENTE",
    PNOMBRE_CLI || ' ' ||
    SNOMBRE_CLI || ' ' ||
    APPATERNO_CLI || ' ' ||
    APMATERNO_CLI AS "NOMBRE COMPLETO",
    TO_CHAR(FECHA_NAC_CLI, 'dd/mm/yyyy') AS "FECHA DE NACIMIENTO"
FROM CLIENTE
WHERE EXTRACT(DAY FROM FECHA_NAC_CLI)= 21 and EXTRACT (MONTH FROM FECHA_NAC_CLI)= 08 
ORDER BY APPATERNO_CLI ASC;
