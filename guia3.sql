--2
SELECT 
    NUMRUN_EMP AS "RUN EMPLEADO",
    PNOMBRE_EMP || ' ' ||
    SNOMBRE_EMP || ' ' ||
    APPATERNO_EMP || ' ' ||
    APMATERNO_EMP AS "NOMBRE COMPLETO EMPLEADO",
    TRUNC(SUELDO_BASE/100000) AS "PORCENTAJE DE MOVILIZACIÓN",
    ROUND(TRUNC(SUELDO_BASE/100000)/100 * SUELDO_BASE) AS "VALOR MOVILIZACIÓN"
FROM EMPLEADO
ORDER BY TRUNC(SUELDO_BASE/100000) DESC;
------------------------------------------
--3
SELECT
    NUMRUN_EMP || '-' ||
    DVRUN_EMP AS "RUN EMPLEADO",
    PNOMBRE_EMP || ' ' ||
    SNOMBRE_EMP || ' ' ||
    APPATERNO_EMP || ' ' ||
    APMATERNO_EMP AS "NOMBRE COMPLETO EMPLEADO",
    SUELDO_BASE AS "SUELDO BASE",
    TO_CHAR (FECHA_NAC, 'dd/mm/yyyy') AS "FECHA DE NACIMIENTO",
    SUBSTR(PNOMBRE_EMP,1,3)|| '' ||
    LENGTH(PNOMBRE_EMP)|| '*' ||
    SUBSTR(TO_CHAR(SUELDO_BASE),
    LENGTH(SUELDO_BASE))||
    DVRUN_EMP||
    TRUNC((MONTHS_BETWEEN(SYSDATE,FECHA_CONTRATO))/12) AS "NOMBRE DE USUARIO",
    SUBSTR(NUMRUN_EMP,3,1)||
    EXTRACT(YEAR FROM FECHA_CONTRATO)+2||
    SUBSTR(SUELDO_BASE,-3)-1||
    SUBSTR(APPATERNO_EMP,-2)||
    EXTRACT(MONTH FROM SYSDATE) AS "CLAVE"
FROM EMPLEADO
ORDER BY APPATERNO_EMP ASC;
